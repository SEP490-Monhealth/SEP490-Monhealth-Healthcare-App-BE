namespace Monhealth.Core.Enum
{
    public enum FoodType
    {
        Carbohydrate, // Tinh bột (gạo, mì, khoai tây, ngũ cốc, ...)
        Protein,      // Đạm (thịt, hải sản, trứng, đậu, ...)
        Vegetables,   // Rau (rau xanh, củ quả, ...)
        Fruits,       // Trái cây (cam, táo, chuối, ...)
        Fats,         // Chất béo (dầu ăn, bơ, các loại hạt, ...)
        Dairy,        // Sản phẩm từ sữa (sữa, phô mai, sữa chua, ...)
        Soup,         // Món canh (canh rau, canh thịt, ...)
        Liquids,      // Nước (nước lọc, nước trái cây, nước uống có calo thấp)
        Snacks,       // Đồ ăn vặt (bánh tráng, nem chua, bánh phồng tôm, ...)
        Desserts      // Món tráng miệng (chè, bánh ngọt, trái cây tráng miệng, ...)
    }
}
